package vn.asg.converter.exceptions;

/**
 *
 * @author ThanhNk
 */
public class UnSupportFormatException extends Exception {
    
    public UnSupportFormatException() {
        super();
    }

    public UnSupportFormatException(String message) {
        super(message);
    }

    public UnSupportFormatException(Throwable ex) {
        super(ex);
    }

    public UnSupportFormatException(String message, Throwable ex) {
        super(message, ex);
    }
    
    
    
}

